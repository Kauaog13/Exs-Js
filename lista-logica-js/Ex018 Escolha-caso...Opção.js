switch(prompt("Escolha uma opção: \n1 - Opção 1 \n2 - Opção 2 \n3 - Opção 3")) {
  case "1":
    alert("Você escolheu a opção 1");
    break;
  case "2":
    alert("Você escolheu a opção 2");
    break;
  case "3":
    alert("Você escolheu a opção 3");
    break;
  default:
    alert("Opção inválida");
}